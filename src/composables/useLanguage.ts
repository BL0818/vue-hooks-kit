export function useLanguage() {
  const t = computed(() => translations[currentLang.value])
  
  const toggleLanguage = () => {
    currentLang.value = currentLang.value === 'en' ? 'zh' : 'en'
  }

  return {
    currentLang,
    t,
    toggleLanguage,
  }
}
